import mongoose, { Schema } from "mongoose";
import Iuser from "../interface/Iuser";

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});
const User = mongoose.model<Iuser>("User", userSchema);
export default User;
